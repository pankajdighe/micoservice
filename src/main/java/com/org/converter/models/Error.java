package com.org.converter.models;

/**
 * Model class to wrap the errors
 */
public class Error {

    private String id;
    private String details;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }


}
